    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        try
        {
            userId=txtUserId.getText();
            ArrayList <User> List=UserDao.getCashier(userId);
            if(List.isEmpty()==true)
                JOptionPane.showMessageDialog(null, "employees not present!","sorry!",JOptionPane.INFORMATION_MESSAGE);
            else
            {
                Object[]rows=new Object[8];
                DefaultTableModel model=(DefaultTableModel)jtUsersDetails.getModel();
                for(User obj:List)
                {
                    rows[0]=obj.getUserId();
                    rows[1]=obj.getUsername();
                    rows[2]=obj.getEmpId();
                    model.addRow(rows);
                }
            }
        }
        catch(SQLException ex)
        {
              JOptionPane.showMessageDialog(null, "Error while searching employees!","Exception!",JOptionPane.ERROR_MESSAGE); 
              ex.printStackTrace();
        }
    }//GEN-LAST:event_btnSearchActionPerformed

