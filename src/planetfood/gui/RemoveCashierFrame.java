    private void btnDeleteCashierActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteCashierActionPerformed
        // TODO add your handling code here:
        try
        {
            userId=txtUserId.getText();
            boolean status=UserDao.RemoveCashier(userId);
           if(status)
           {
                JOptionPane.showMessageDialog(null, "Cashier successfully deleted!","Success!",JOptionPane.INFORMATION_MESSAGE);
                clearAll();
                
           }
           else
              JOptionPane.showMessageDialog(null, "Product not deleted!","Updation Denied!",JOptionPane.ERROR_MESSAGE);
            
        }
        catch(SQLException ex)
        {
              JOptionPane.showMessageDialog(null, "Error while deleting product!","Exception!",JOptionPane.ERROR_MESSAGE); 
              ex.printStackTrace();
        }
    }//GEN-LAST:event_btnDeleteCashierActionPerformed

