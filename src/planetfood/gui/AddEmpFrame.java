    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
        // TODO add your handling code here:
        if(!validateInputs())
        {
            JOptionPane.showMessageDialog(null,"please fill all the data","Empty Fields!",JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        try
        {
            double sal=Double.parseDouble(salary);
            Emp emp=new Emp();
            emp.setEmpid(empid);
            emp.setEname(empname);
            emp.setJob(Job);
            emp.setSal(sal);
            boolean result=EmpDao.addEmployees(emp);
            if(result)
            {
                JOptionPane.showMessageDialog(null,"Record inserted","Successfully!",JOptionPane.INFORMATION_MESSAGE);
                clearAll();
            }
            else
                JOptionPane.showMessageDialog(null,"please fill all the data","Empty Fields!",JOptionPane.INFORMATION_MESSAGE);
        }
            catch(NumberFormatException e)
                        {
                        JOptionPane.showMessageDialog(null,"connection Not Done","Error!",JOptionPane.ERROR_MESSAGE);
                        e.printStackTrace();
                        }
                catch(SQLException e)
                        {
                        JOptionPane.showMessageDialog(null,"connection Not Done","Error!",JOptionPane.ERROR_MESSAGE);
                        e.printStackTrace();
                        }
    }//GEN-LAST:event_btnRegisterActionPerformed

    public void clearAll()
    {
        txtEmpId.setText("");
        txtEmpName.setText("");
        txtSalary.setText("");
        try
        {
        txtEmpId.setText(EmpDao.getNewiD());
        }
        catch(SQLException ex)
        {
            JOptionPane.showMessageDialog(null, "Exception while connectin to DB","Exception!",JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
        jcJob.setSelectedIndex(0);
        txtEmpId.requestFocus();
    }
     public boolean validateInputs()
    {
        empid=txtEmpId.getText();
        empname=txtEmpName.getText();
        Job=jcJob.getSelectedItem().toString();
        salary=txtSalary.getText();
        if(empid.isEmpty()||empname.isEmpty()||salary.isEmpty())
            return false;
        return true;
    }
